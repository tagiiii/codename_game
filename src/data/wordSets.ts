/**
 * ランダムな単語セットを取得
 */
export const getRandomWordSet = (): WordSet => {
  const randomIndex = Math.floor(Math.random() * wordSets.length);
  return wordSets[randomIndex];
};

/**
 * IDで単語セットを取得
 */
export const getWordSetById = (id: string): WordSet | undefined => {
  return wordSets.find((set) => set.id === id);
};
